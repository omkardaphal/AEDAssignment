    private void selectFile() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setAcceptAllFileFilterUsed(false);// needed false to selec limited extension files
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Images", "jpg");// only jpg files.
        fileChooser.addChoosableFileFilter(filter);
        fileChooser.showOpenDialog(this);
        
        try {
            img = ImageIO.read(fileChooser.getSelectedFile());
            int w = img.getWidth();
            int h = img.getHeight();
            
            if (w > 150 || h > 150) {
                JOptionPane.showMessageDialog(this, "Image must be of maximum 150 x 150 pixels resolution.. Try again");
                selectFile();
                
            } else {
                ImageIcon icon = new ImageIcon(fileChooser.getSelectedFile().getAbsolutePath());
                img_path = fileChooser.getSelectedFile().getAbsolutePath();
                photoLabel.setIcon(icon);
            }
            
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Not valid image. Try again");
            selectFile();
            ex.printStackTrace();
        }
        
    }

