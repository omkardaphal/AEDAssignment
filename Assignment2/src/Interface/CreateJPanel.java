    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        try {
            int year, month, day;
            
            year = Integer.parseInt(yearCombo.getSelectedItem().toString());//converting string to integer 
            month = Integer.parseInt(monthCombo.getSelectedItem().toString());
            day = Integer.parseInt(dayCombo.getSelectedItem().toString());
            if (ValidateDate.isDateValid(year, month, day)) {
                DOB dob = new DOB(year, month, day);
                
                if (nameTextField.getText().trim().length() != 0) {
                    res.setName(nameTextField.getText());
                } else {
                    JOptionPane.showMessageDialog(this, "Enter your name");
                    return;
                }
                res.setDob(dob);
                
                if (addTextArea.getText().trim().length() != 0) {
                    res.setAddress1(addTextArea.getText());
                } else {
                    JOptionPane.showMessageDialog(this, "Enter your address");
                    return;
                }
                if (designationTextField.getText().trim().length() != 0) {
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Enter expected Designation");
                    return;
                }
                
                if (addTextArea.getText().trim().length() != 0) {
                    res.setDesg(designationTextField.getText());
                } else {
                    JOptionPane.showMessageDialog(this, "Enter your text area");
                    return;
                }
                
                if (phoneTextField.getText().trim().length() != 0 && phoneTextField.getText().length() == 10) {
                    res.setPhNo(phoneTextField.getText());
                } else {
                    JOptionPane.showMessageDialog(this, "Enter your 10 digits Phone Number");
                    return;
                }
                
                if (img_path.trim().length() != 0) {
                    res.setImage_path(img_path);
                } else {
                    JOptionPane.showMessageDialog(this, "Your image is required");
                    return;
                }
                if (affiliationTextField.getText().trim().length() != 0) {
                    res.setAffilation(affiliationTextField.getText());
                } else {
                    JOptionPane.showMessageDialog(this, "Enter Affilation");
                    return;
                }

                //adding education
                if (Double.parseDouble(gpa1TextField.getText()) <= 4.0) {
                    modal.Education edu = new modal.Education(coll1TextField.getText(), degree1TextField.getText(), Double.parseDouble(gpa1TextField.getText()), passingTextField.getText());
                    res.addEdu(edu);
                } else {
                    JOptionPane.showMessageDialog(this, "GPA must be less than 4.0");
                    return;
                }
                if (coll2TextField.getText().trim().length() != 0 && degree2TextField.getText().trim().length() != 0) {
                    if (Double.parseDouble(gpa2TextField.getText()) <= 4.0) {
                        modal.Education edu1 = new modal.Education(coll2TextField.getText(), degree2TextField.getText(), Double.parseDouble(gpa2TextField.getText()), passing2TextField.getText());
                        res.addEdu(edu1);
                    } else {
                        JOptionPane.showMessageDialog(this, "GPA must be less than 4.0");
                        return;
                    }
                }

                //adding work
                if (expYesRadioButton.isSelected()) {
                    if (org1TextField.getText().trim().length() != 0 && desg1TextField.getText().trim().length() != 0) {
                        Work_Exp work = new Work_Exp(org1TextField.getText(), desg1TextField.getText(), Double.parseDouble(dur1TextField.getText()));
                        res.addWork(work);
                    } else {
                        JOptionPane.showMessageDialog(this, "Add work Experience");
                    }
                    if (org2TextField.getText().trim().length() != 0 && desg2TextField.getText().trim().length() != 0) {
                        Work_Exp work = new Work_Exp(org2TextField.getText(), desg2TextField.getText(), Double.parseDouble(dur2TextField.getText()));
                        res.addWork(work);
                    }
                    
                } else if (expNoRadioButton.isSelected()) {
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Do you have experience. Please select");
                    return;
                }

                //adding gender
                if (genderMaleRadioButton1.isSelected()) {
                    res.setGender("Male");
                } else if (genderFemaleRadioButton.isSelected()) {
                    res.setGender("Female");
                } else {
                    JOptionPane.showMessageDialog(this, "Please select Your gender");
                    return;
                }

                //adding joining
                if (joinImmediateRadioButton.isSelected()) {
                    res.setJoining(joinImmediateRadioButton.getText());
                } else if (join1MonthRadioButton.isSelected()) {
                    res.setJoining(join1MonthRadioButton.getText());
                } else if (join2MonthRadioButton.isSelected()) {
                    res.setJoining(join2MonthRadioButton.getText());
                } else if (join3MonthRadioButton.isSelected()) {
                    res.setJoining(join3MonthRadioButton.getText());
                } else {
                    JOptionPane.showMessageDialog(this, "Select Joining Option");
                    return;
                }

                //adding skills
                if (CheckBoxCpp.isSelected()) {
                    res.addSkills("C++");
                }
                if (CheckBoxJava.isSelected()) {
                    res.addSkills("Java");
                }
                if (CheckBoxMsOffice.isSelected()) {
                    res.addSkills("MS OFfice");
                }
                if (CheckBoxSql.isSelected()) {
                    res.addSkills("SQL");
                }
                if (CheckBoxTesting.isSelected()) {
                    res.addSkills("Testing");
                }
                if (CheckBoxWDev.isSelected()) {
                    res.addSkills("Web Development");
                }
                System.out.println("asd");
                JOptionPane.showMessageDialog(this, "Submitted. Click on create button to add another");
                
            } else {
                JOptionPane.showMessageDialog(this, "Please Enter Valid DateF");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Something went wrong");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_submitBtnActionPerformed

